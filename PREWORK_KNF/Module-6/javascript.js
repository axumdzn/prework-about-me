function toGrow() {
    document.getElementById("box").style.height = "250px";
    document.getElementById("box").style.width = "250px";
}
function beBlue() {
    document.getElementById("box").style.backgroundColor = "blue";
}
function toFade() {
    document.getElementById("box").style.opacity = ".5";
}
